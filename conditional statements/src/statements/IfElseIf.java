package statements;

public class IfElseIf {

	public static void main(String[] args) {
		int a=5;
		int b=10;
		if(a<b) {
			System.out.println(a+" is lesser than "+b);
		}
		else if(a>b){
			System.out.println(a+" is greater than "+b);
		}
		else {
			System.out.println(a+" is equal to "+b);
		}

	}

}
